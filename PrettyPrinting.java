package com.thakur;

public class PrettyPrinting {
    public static void main(String[] args) {
        float a = 436.679f;
        System.out.printf("Formated no. is %.2f\n", a);// percentage means place holder
        System.out.printf("%.3f\n",Math.PI);
        System.out.printf("Hello, my name is %s", "Ayush Thakur");
    }
}
